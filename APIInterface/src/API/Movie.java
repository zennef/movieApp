package API;

public class Movie {
	private String title;
	private String director;
	private String posterUrl;
	private String redBoxUrl;
	private String itunesUrl;
	private String itunesBuyPrice;
	private String itunesRentPrice;
	private String rating;
	
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDirector() {
		return director;
	}
	public void setDirector(String director) {
		this.director = director;
	}
	public String getPosterUrl() {
		return posterUrl;
	}
	public void setPosterUrl(String posterUrl) {
		this.posterUrl = posterUrl;
	}
	public String getRedBoxUrl() {
		return redBoxUrl;
	}
	public void setRedBoxUrl(String redBoxUrl) {
		this.redBoxUrl = redBoxUrl;
	}
	public String getItunesUrl() {
		return itunesUrl;
	}
	public void setItunesUrl(String itunesUrl) {
		this.itunesUrl = itunesUrl;
	}
	public String getRating() {
		return rating;
	}
	public void setRating(String rating) {
		this.rating = rating;
	}
	public String getItunesBuyPrice() {
		return itunesBuyPrice;
	}
	public void setItunesBuyPrice(String itunesBuyPrice) {
		this.itunesBuyPrice = itunesBuyPrice;
	}
	public String getItunesRentPrice() {
		return itunesRentPrice;
	}
	public void setItunesRentPrice(String itunesRentPrice) {
		this.itunesRentPrice = itunesRentPrice;
	}
	
	

}
